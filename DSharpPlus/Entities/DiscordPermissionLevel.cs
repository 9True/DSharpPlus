namespace DSharpPlus.Entities;

/// <summary>
/// Whether a permission is allowed, denied or unset
/// </summary>
public enum DiscordPermissionLevel
{
    /// <summary>
    /// Said permission is Allowed
    /// </summary>
    Allowed,

    /// <summary>
    /// Said permission is Denied
    /// </summary>
    Denied,

    /// <summary>
    /// Said permission is Unset
    /// </summary>
    Unset
}
